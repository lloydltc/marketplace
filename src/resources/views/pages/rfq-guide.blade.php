    <p>Can't find the exact part? Post a request and let sellers come to you. Describe what you need and receive quotes you can compare and accept.</p>

    <h2 class="text-lg font-semibold text-neutral-900">How it works</h2>
    <ol class="list-decimal list-inside">
        <li>Post a request describing the part, your vehicle, and your location.</li>
        <li>Sellers review open requests and send you quotes.</li>
        <li>Accept the quote that suits you — it converts into a normal order with standard buyer protection.</li>
    </ol>

    <h2 class="text-lg font-semibold text-neutral-900">Fair use</h2>
    <p>A monthly free quota of requests applies; see the <a href="{{ route('pages.fees') }}" class="text-[#3DB8E8] hover:underline">Fees</a> page for any overage charge. High-value requests may require a small refundable commitment deposit.</p>

