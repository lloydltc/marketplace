<x-content-page title="How Fulfilled by Salma (FBS) Works" metaDescription="FBS is SalmaDrive's end-to-end fulfilment: we store, pack, and deliver, so buyers shop with confidence.">
    <p>Fulfilled by Salma (FBS) is our end-to-end fulfilment service. When you buy an FBS item, SalmaDrive handles payment, delivery, and buyer protection.</p>

    <h2 class="text-lg font-semibold text-neutral-900">For buyers</h2>
    <ol class="list-decimal list-inside">
        <li>Add an FBS item to your cart and check out.</li>
        <li>Pay online, or use Cash on Delivery where available.</li>
        <li>Our rider delivers to your address and the order is tracked end to end.</li>
    </ol>

    <h2 class="text-lg font-semibold text-neutral-900">For sellers</h2>
    <p>FBS listings reach more buyers with the trust of Salma-managed delivery. Delivery fees and platform commission are shown on the <a href="{{ route('pages.fees') }}" class="text-[#3DB8E8] hover:underline">Fees</a> page; proceeds settle to your wallet after completion.</p>

    <h2 class="text-lg font-semibold text-neutral-900">Vendor-fulfilled alternative</h2>
    <p>Sellers may also fulfil orders themselves (VF). COD on VF orders requires the vendor to be in good wallet standing.</p>
