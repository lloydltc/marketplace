<x-content-page title="Cash on Delivery (COD) Policy" metaDescription="How cash on delivery works on SalmaDrive, when it's available, and buyer/seller responsibilities.">
    <p>Cash on Delivery lets eligible buyers pay when their order arrives. Availability depends on the fulfilment method and seller standing.</p>

    <h2 class="text-lg font-semibold text-neutral-900">When COD is available</h2>
    <ul class="list-disc list-inside">
        <li><strong>Fulfilled by Salma (FBS):</strong> COD is offered when enabled for the item and the delivery zone supports it.</li>
        <li><strong>Vendor-fulfilled (VF):</strong> COD requires the vendor to be in good wallet standing (above the wallet floor).</li>
    </ul>

    <h2 class="text-lg font-semibold text-neutral-900">On delivery</h2>
    <p>Please have the exact amount ready. For FBS-COD, our rider collects and reconciles the cash; the order is confirmed once collection is recorded.</p>

    <h2 class="text-lg font-semibold text-neutral-900">Refusal &amp; abuse</h2>
    <p>Repeated refusal of COD orders may result in COD being disabled for your account. See the <a href="{{ route('pages.fees') }}" class="text-[#3DB8E8] hover:underline">Fees</a> page for delivery charges.</p>
</x-content-page>
