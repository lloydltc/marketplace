<x-content-page title="Privacy Policy" metaDescription="How SalmaDrive collects, uses, and protects your personal data." updated="{{ date('F Y') }}">
    <p>This policy explains what data SalmaDrive collects and how we handle it.</p>

    <h2 class="text-lg font-semibold text-neutral-900">What we collect</h2>
    <p>Account details (name, email, phone), listing content, order and delivery information, and payment references. We do not store full card details — payments are handled by our gateway.</p>

    <h2 class="text-lg font-semibold text-neutral-900">How we protect it</h2>
    <p>Sensitive data such as bank account numbers is encrypted at rest. Access to personal data is restricted by role, and privileged actions are audit-logged. Connections are served over HTTPS.</p>

    <h2 class="text-lg font-semibold text-neutral-900">How we use it</h2>
    <p>To operate the marketplace: process orders and payouts, deliver goods, prevent fraud, and communicate with you about your account and transactions.</p>

    <h2 class="text-lg font-semibold text-neutral-900">Your choices</h2>
    <p>You may request a copy of your data or ask us to delete your account and associated personal data, subject to records we must retain for legal and financial reconciliation. Email <a href="mailto:info@salmadrive.co.zw" class="text-[#3DB8E8] hover:underline">info@salmadrive.co.zw</a>.</p>

    <h2 class="text-lg font-semibold text-neutral-900">Retention</h2>
    <p>Transaction and ledger records are retained as required for financial reconciliation and compliance; other personal data is removed on account deletion.</p>
